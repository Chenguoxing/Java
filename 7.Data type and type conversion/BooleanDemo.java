//Boolean数据类型
public class BooleanDemo

{
	public static void main(String[] args) 
	{
		//boolean的常量
		System.out.println(true);
		System.out.println(false);
		System.out.println("----------------------");
		
		//boolean的变量
		boolean isMen = true;
		System.out.println(isMen);
		 isMen = false;
		System.out.println(isMen);
	}
}
